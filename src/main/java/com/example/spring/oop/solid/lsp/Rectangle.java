package com.example.spring.oop.solid.lsp;

// 직사각형
public class Rectangle {

    private int width;

    private int height;

    public void setWidth(int width) {
        this.width = width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
